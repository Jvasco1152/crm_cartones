const { PrismaClient } = require('@prisma/client')
const prisma = new PrismaClient()

// Helper: inserta solo si no existe ya por empresa o codigoCliente
async function upsert(data) {
  const existe = await prisma.cliente.findFirst({
    where: {
      OR: [
        { empresa: { equals: data.empresa, mode: 'insensitive' } },
        ...(data.codigoCliente ? [{ codigoCliente: data.codigoCliente }] : []),
      ],
    },
  })
  if (existe) {
    console.log(`  SKIP (ya existe): ${data.empresa}`)
    return null
  }
  const c = await prisma.cliente.create({ data })
  console.log(`  + AGREGADO: ${data.empresa}`)
  return c
}

